function startTimeline(fase, nextfase) {
    var last;
    jQuery.getJSON(c80.timelineurl, function (data) {
        if (isMobile()) {
            console.log(isMobile());
            var tloptions = {
                language: "es",
                hash_bookmark: true,
                group_order: [
                    "Constitucional",
                    "Poltico social",
                    "Presidencial",
                ],
                max_rows: 3,
                timenav_mobile_height_percentage: 25,
                timenav_height_min: 160,
                marker_height_min: 14,
            };
        } else {
            var tloptions = {
                language: "es",
                hash_bookmark: true,
                group_order: [
                    "Constitucional",
                    "Poltico social",
                    "Presidencial",
                ],
                max_rows: 3,
            };
        }

        timelineObj = new TL.Timeline(
            "timeline-js-container",
            data[fase],
            tloptions
        );
        //console.log(data);

        var events = data[fase].events;
        var lastSlide = timelineObj.getSlide(events.length - 1);
        var firstSlideHash = timelineObj.getSlide(0).data.unique_id;

        var hash = window.location.hash.substr(1);
        var hashfase = c80.timelinehitosfases[hash];

        //console.log(hashfase);
        if (hashfase == undefined) {
            updateHash(firstSlideHash);
            buildShareLinks(window.location);
        }

        last = data[fase].lastevent;

        timelineObj.on("change", function (data) {
            console.log("change");
            hash = data.unique_id;

            updateHash(hash);

            buildShareLinks(window.location);

            if (data.unique_id == lastSlide.data.unique_id) {
                console.log(nextfase);
                var next = $(
                    'section.presentacion-fase[data-fase="' + nextfase + '"]'
                ).attr("id");
                var nextText = $(
                    '#timeline-nav li a[href="#' + next + '"] h1'
                ).text();

                if (next) {
                    if (isMobile()) {
                        jQuery("div.timeline-mobile-header").addClass(
                            "shownext"
                        );
                        jQuery("div.nextphase-mobile").addClass("active");
                        jQuery("div.nextphase-mobile a").attr(
                            "href",
                            "#" + next
                        );
                        jQuery("div.nextphase-mobile a span.range").text(
                            ": " + nextText
                        );
                        jQuery("div.mobile-interaction")
                            .empty()
                            .append(
                                '<a class="btn-nextphase" data-toggle="nextphase" href="#' +
                                    next +
                                    '"><span class="msg">Prximo perodo</span> <i class="fa fa-angle-right"></i></a>'
                            );
                    } else {
                        jQuery(".tl-storyslider").append(
                            '<a class="btn-nextphase" data-toggle="nextphase" href="#' +
                                next +
                                '"><i class="fa fa-angle-right"></i></a>'
                        );
                    }
                }
            } else {
                jQuery(".tl-storyslider .btn-nextphase").remove();
                restoreMobileTLHeader();
            }
        });
    });
}

function closest(array, number) {
    var num = 0;
    for (var i = array.length - 1; i >= 0; i--) {
        if (
            Math.abs(number - array[i].position) <
            Math.abs(number - array[num].position)
        ) {
            num = i;
        }
    }
    return array[num].element;
}

function nextPhase(fases, fase) {
    nextphase = false;

    for (var i = 0; i < fases.length; i++) {
        if (fases[i] == fase) {
            if (fases[i + 1] !== undefined) {
                nextphase = fases[i + 1];
            }
        }
    }
    return nextphase;
}

function customvh() {
    // First we get the viewport height and we multiple it by 1% to get a value for a vh unit
    let vh = window.innerHeight * 0.01;
    console.log(vh);
    // Then we set the value in the --vh custom property to the root of the document
    document.documentElement.style.setProperty("--vh", `${vh}px`);
}

function buildShareLinks(location) {
    $(".sharers a").each(function () {
        var shareHref = location.href.replace("#", "%23");
        var shareUrl = $(this).attr("data-base") + shareHref;

        $(this).attr("href", shareUrl);
    });
}

function updateHash(hash) {
    if (history.pushState) {
        history.pushState(null, null, "#" + hash);
    } else {
        location.hash = "#" + hash;
    }
}
